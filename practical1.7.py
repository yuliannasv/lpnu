a = int(input("Введіть перше ціле число (a): "))
b = int(input("Введіть друге ціле число (b): "))

if 1 <= a <= 1000 and 1 <= b <= 1000:
    max_value = (a + b + abs(a - b)) // 2
    print(f"Найбільше з чисел {a} і {b} = {max_value}")
else:
    print("Числа повинні бути в межах від 1 до 1000.")